package com.example.agrotrade.Model;

public class Crop_Info {
        private String name,img,season,irrigation,varieties,soil_type,plant_material,spacing,harvesting;

        public String getName() {
            return name;
        }

        public void setName(String name) {
        this.name = name;
    }

        public String getImg() { return img; }

        public void setImg(String img) {
        this.img = img;
    }

        public String getSeason() {
            return season;
        }

        public void setSeason(String season) {
            this.season = season;
        }

        public String getIrrigation() {
            return irrigation;
        }

        public void setIrrigation(String irrigation) {
            this.irrigation = irrigation;
        }

        public String getVarieties() {
            return varieties;
        }

        public void setVarieties(String varieties) {
            this.varieties = varieties;
        }

        public String getSoil_type() {
            return soil_type;
        }

        public void setSoil_type(String soil_type) {
            this.soil_type = soil_type;
        }

        public String getPlant_material() {
            return plant_material;
        }

        public void setPlant_material(String plant_material) {
            this.plant_material = plant_material;
        }

        public String getSpacing() {
            return spacing;
        }

        public void setSpacing(String spacing) {
            this.spacing = spacing;
        }

    public String getHarvesting() {
        return harvesting;
    }

    public void setHarvesting(String harvesting) {
        this.harvesting = harvesting;
    }
}
